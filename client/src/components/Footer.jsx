import React from 'react';

const Footer = ({ themes, onChange }) => {
  const formatThemes = themes => {
    return themes.map((t, idx) => (
      <option key={idx} value={t.themeShort}>{t.themeLong}</option>
    ));
  };

  return ( 
    <footer id='Footer'>
      <nav id='footer-navigation'>
        <div>
          <a href="#top">top</a>
        </div>
        <div>
          <label>
            Theme:
            <select id='style-selector' onChange={onChange} >
              {formatThemes(themes)}
            </select>
          </label>
        </div>
      </nav>
      <div id='footer-disclaimer'>
        <span>arnest00 &copy; 2021</span>
        <br />
        <span>All deltaChannel posts are owned and generated by its users.</span>
      </div>
    </footer>
  );
};
 
export default Footer;